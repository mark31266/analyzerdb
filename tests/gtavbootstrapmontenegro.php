              <h1>Grant Theft Auto V</h1>

              <p>
              Grand Theft Auto V is a 2013 open world action-adventure video game developed by Rockstar North and published by Rockstar Games. It was released on 17 September 2013 for the PlayStation 3 and Xbox 360 consoles. It is the fifteenth title in the Grand Theft Auto series, and the first main entry since Grand Theft Auto IV in 2008. As one of the last titles to be released exclusively for the seventh generation of video game consoles, Grand Theft Auto V was highly anticipated preceding its release.
              </p>

              <h2>Gameplay</h2>
              <p>
                Grand Theft Auto V is an action-adventure video game played from a third-person perspective in an open world environment. The player uses melee attacks, firearms, weapons and explosives to fight enemies, and can run, jump, swim, or use vehicles to navigate the game's world. In combat, auto aim and a cover system can be used as assistance against enemies.
              </p>
              <p>
                Each character has a set of eight skills, which are numerical representations of their ability in certain areas, such as shooting and driving. While the player improves the skills of each character as they play, there is a skill assigned to each character that they have expertise in by default, such as Trevor's skill as a pilot.
              </p>
